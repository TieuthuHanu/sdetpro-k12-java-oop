package lesson_03;

public class ForLoopLearning {

    public static void main(String[] args) {

        int maxNum = 10;

        // Infinitive loop
        for (int i = 0; i < maxNum; ) {
            System.out.println(maxNum);
        }

        for ( ; ; ) {
            System.out.println(maxNum);
        }

    }
}
